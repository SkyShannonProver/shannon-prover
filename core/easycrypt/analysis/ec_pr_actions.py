"""Pr-layer action rendering for EasyCrypt ProofIR.

This module turns already-computed Pr front-end facts into menu actions.  It
does not discover lemmas, inspect EasyCrypt, or plan paths; those compiler
passes live in the Pr obligation/index/path modules.  Keeping rendering here
lets ProofIR own orchestration without accumulating Pr-specific if/else blocks.
"""
from __future__ import annotations

from typing import Any

from core.easycrypt.analysis.ec_utils import (
    as_dict as _dict,
    as_list as _list,
    lemma_leaf as _lemma_leaf,
)
from core.easycrypt.analysis.ec_menu_actions import menu_item
from core.easycrypt.analysis.ec_name_resolution import resolution_for_name


PR_ACTIONS_SCHEMA_VERSION = 1
PR_ACTIONS_KIND = "easycrypt_pr_actions"


def visible_pr_rewrites(handles: dict[str, Any]) -> list[Any]:
    recent_rewrite = str(_dict(handles).get("recently_rewritten_pr_lemma") or "")
    return [
        item for item in _list(_dict(handles).get("pr_rewrite_candidates"))
        if _lemma_leaf(str(item)) != recent_rewrite
    ]


def pr_normalization_menu_items(handles: dict[str, Any]) -> list[dict[str, Any]]:
    pr_normal = _dict(_dict(handles).get("pr_normal_form"))
    if not pr_normal.get("available"):
        return []
    normal_kind = str(pr_normal.get("normalization_kind") or "")
    recommended_tactic = str(pr_normal.get("recommended_tactic") or "")
    if recommended_tactic:
        return [menu_item(
            "pr_normalize_congr",
            tactic=recommended_tactic,
            tactic_family="pr_normalization",
            action_type="probe_tactic",
            cost="cheap",
            why=(
                str(pr_normal.get("reason") or "")
                or "Normalize the Pr goal's outer arithmetic shell when it clarifies the route choice."
            ),
            preconditions=[
                "introduce all leading premises/binders first",
                "use this when the normalized Pr form makes rewrite/path/byequiv choice clearer",
            ],
            preserves=["Pr abstraction"],
            cost_factors={
                "normalization_kind": normal_kind,
                "goal_form": str(pr_normal.get("goal_form") or ""),
            },
        )]
    return []


def pr_decomposition_bridge_menu_items(
    handles: dict[str, Any],
) -> list[dict[str, Any]]:
    obligation = _dict(_dict(handles).get("pr_decomposition_bridge_obligation"))
    if not obligation.get("available"):
        return []
    return [menu_item(
        "pr_direct_byequiv_bridge",
        tactic="byequiv => //.",
        tactic_family="probability_to_program",
        action_type="probe_tactic",
        cost="cheap",
        why=str(
            obligation.get("reason")
            or (
                "The current Pr equality is the first bridge obligation "
                "generated by a have/decomposition step."
            )
        ),
        preconditions=[
            "use this when the current bridge obligation is the endpoint being tested",
            "after byequiv, open procedure bodies only as needed for this bridge subgoal",
        ],
        preserves=["outer Pr proof plan"],
        cost_factors={
            "obligation_source": "latest_have_decomposition",
        },
    )]


def pr_byequiv_fallback_menu_items(
    handles: dict[str, Any],
    *,
    goal_kind: str,
    pr_rewrites: list[Any],
    pr_bridge_obligation: dict[str, Any],
    has_live_typed_bridge_path: bool,
    has_runnable_pr_frontend: bool,
) -> list[dict[str, Any]]:
    frontend = _dict(_dict(handles).get("pr_byequiv_frontend"))
    if (
        goal_kind not in {"Pr_eq", "Pr_ineq"}
        or not frontend.get("available")
        or pr_bridge_obligation.get("available")
        or has_live_typed_bridge_path
    ):
        return []
    fallback_action_type = (
        "strategy_hint" if has_runnable_pr_frontend else "probe_tactic"
    )
    fallback_reason = (
        str(frontend.get("reason") or "") if has_runnable_pr_frontend else ""
    )
    if not fallback_reason:
        fallback_reason = (
            "No complete typed Pr path or runnable Pr rewrite is currently "
            "available; pRHL lowering is a probeable alternative while "
            "partial Pr handles remain contextual."
            if not has_runnable_pr_frontend else
            "The Pr front-end sees direct pRHL lowering as a fallback for "
            "this probability goal."
        )
    return [menu_item(
        "pr_byequiv_fallback",
        tactic="byequiv => //.",
        tactic_family="probability_to_program",
        action_type=fallback_action_type,
        cost="moderate",
        why=fallback_reason,
        preconditions=[
            "compare exact Pr normalization/rewrite/path candidates by endpoint match and simplification",
            "use this when direct program equivalence is the natural route, or when Pr handles are partial, ambiguous, or noisy",
        ],
        preserves=["Pr goal intent"],
        cost_factors={
            "fallback_after_pr_handles": bool(pr_rewrites),
            "has_runnable_pr_frontend": has_runnable_pr_frontend,
        },
    )]


def pr_rewrite_menu_items(
    handles: dict[str, Any],
    *,
    goal_kind: str,
    pr_rewrites: list[Any],
) -> list[dict[str, Any]]:
    if not pr_rewrites:
        return []
    lemma = str(pr_rewrites[0])
    resolved = resolution_for_name(_dict(_dict(handles).get("name_resolution")), lemma)
    lookup_action = str(resolved.get("signature_lookup_action") or "")
    needs_instantiation = bool(
        resolved.get("exact_signature_known")
        and resolved.get("requires_instantiation")
    )
    needs_lookup = bool(resolved and not resolved.get("exact_signature_known"))
    native_search_available = bool(
        _list(_dict(_dict(handles).get("native_ast_search")).get("hits"))
        or _list(_dict(_dict(handles).get("native_ast_search")).get("suggested_queries"))
    )
    bound_goal_context = (
        goal_kind == "Pr_ineq"
        and native_search_available
        and not _dict(_dict(_dict(handles).get("pr_path_plan")).get("recommended_path"))
    )
    rewrite_cost = "expensive" if bound_goal_context and needs_lookup else "cheap"
    rewrite_why = (
        f"Pr-level rewrite candidate `{lemma}` is available as route context; "
        "use it when it matches the visible endpoint and simplifies the event."
    )
    if bound_goal_context and needs_lookup:
        rewrite_why = (
            f"`{lemma}` is an equality/bridge handle, but the current "
            "frontier is a Pr upper-bound goal. Treat it as context; first "
            "inspect native bound lemmas or the bound/procedure proof plan."
        )
    items = [menu_item(
        "rewrite_pr_handle",
        tactic=f"rewrite {lemma}.",
        tactic_family="rewrite",
        action_type=(
            "strategy_hint" if needs_lookup or needs_instantiation else "probe_tactic"
        ),
        cost=rewrite_cost,
        why=rewrite_why,
        preserves=["Pr abstraction"],
        preconditions=resolution_preconditions(
            lemma,
            resolved,
            lookup_action,
            action_word="rewrite",
        ),
        cost_factors={
            "bound_goal_context": bound_goal_context,
            "name_resolution_status": str(resolved.get("resolution_status") or ""),
        },
    )]
    if resolved and not resolved.get("exact_signature_known") and lookup_action:
        lookup_cost = "expensive" if bound_goal_context else "free"
        lookup_why = (
            f"Resolve exact signature for `{lemma}` only when this rewrite/apply route is being tested."
        )
        if bound_goal_context:
            lookup_why = (
                f"Resolve `{lemma}` only if the Pr upper-bound plan needs "
                "endpoint normalization; this is secondary to bound-lemma "
                "search for the current frontier."
            )
        items.append(menu_item(
            f"sig_{lemma}",
            tactic=lookup_action,
            tactic_family="signature_lookup",
            action_type="inspection_action",
            cost=lookup_cost,
            why=lookup_why,
            preserves=["proof state"],
            cost_factors={
                "bound_goal_context": bound_goal_context,
                "name_resolution_status": str(
                    resolved.get("resolution_status") or ""
                ),
            },
        ))
    return items




def pr_clone_bound_apply_menu_items(handles: dict[str, Any]) -> list[dict[str, Any]]:
    items: list[dict[str, Any]] = []
    for idx, candidate in enumerate(
        _list(_dict(handles).get("pr_clone_bound_apply_candidates"))
    ):
        if not isinstance(candidate, dict):
            continue
        tactic = str(candidate.get("tactic") or "")
        lemma = str(candidate.get("lemma") or "")
        if not tactic or not lemma:
            continue
        items.append(menu_item(
            f"pr_clone_bound_apply_{idx}",
            tactic=tactic,
            tactic_family="pr_path_plan",
            action_type="strategy_hint",
            cost="cheap",
            why=str(
                candidate.get("reason")
                or "Current Pr bound exposes a cloned bound theorem shape."
            ),
            preconditions=[
                f"inspect `{lemma}` if the clone-generated theorem arity is not known",
                "fill any remaining losslessness/module premises generated by the apply",
            ],
            preserves=["Pr arithmetic layer"],
            cost_factors={
                "lemma": lemma,
                "wrapper": str(candidate.get("wrapper") or ""),
                "root": str(candidate.get("root") or ""),
                "memory": str(candidate.get("memory") or ""),
            },
        ))
    return items


def resolution_preconditions(
    lemma: str,
    resolved: dict[str, Any],
    lookup_action: str,
    *,
    action_word: str,
) -> list[str]:
    if resolved.get("exact_signature_known"):
        out = [f"name resolution has exact signature for `{lemma}`"]
        if resolved.get("requires_instantiation"):
            out.append(
                f"instantiate required signature slots before probing the {action_word}"
            )
        return out
    return (
        [f"inspect `{lookup_action}` before guessing arguments"]
        if lookup_action else
        []
    )

