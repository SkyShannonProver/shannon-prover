def _missing_invariant_conjuncts(
    proof_ir: dict[str, Any],
    tactic: str,
) -> list[str]:
    attempted = _normalize_invariant_text(tactic)
    expected: list[str] = []
    resources = _dict(proof_ir.get("resources"))
    handles = _dict(resources.get("handles"))
    for key in ("invariant_skeleton", "adversary_invariant_skeleton"):
        skeleton = _dict(handles.get(key))
        expected.extend([
            str(item) for item in _list(skeleton.get("conjuncts"))
            if str(item)
        ])
        suggested = str(skeleton.get("suggested_invariant") or "")
        if suggested:
            expected.extend(_split_conjuncts(suggested))
    for item in _program_slices(proof_ir):
        invariant = _dict(item.get("invariant_skeleton"))
        expected.extend([
            str(piece) for piece in _list(invariant.get("conjuncts"))
            if str(piece)
        ])
    missing: list[str] = []
    for piece in _dedupe([_clean_conjunct(item) for item in expected if item]):
        if not piece:
            continue
        if _conjunct_present(piece, attempted):
            continue
        missing.append(piece)
    return missing


def _normalize_invariant_text(tactic: str) -> str:
    text = str(tactic or "")
    match = re.search(r"\b(?:e?call)\s*\(\s*_?\s*:\s*(.*?)\)\s*\.?\s*$", text)
    if match:
        text = match.group(1)
    return _clean_conjunct(text)


def _split_conjuncts(text: str) -> list[str]:
    return [
        _clean_conjunct(part)
        for part in _split_flat_conjuncts(text)
        if _clean_conjunct(part)
    ]


def _clean_conjunct(text: str) -> str:
    value = re.sub(r"\s+", " ", str(text or "").strip())
    while value.startswith("(") and value.endswith(")"):
        value = value[1:-1].strip()
    return value


def _conjunct_present(piece: str, attempted: str) -> bool:
    if not piece:
        return True
    if piece in attempted:
        return True
    compact_piece = re.sub(r"\s+", "", piece)
    compact_attempted = re.sub(r"\s+", "", attempted)
    return bool(compact_piece and compact_piece in compact_attempted)


def _pr_path_edges_for_names(
    proof_ir: dict[str, Any],
    names: list[str],
) -> list[dict[str, Any]]:
    wanted = set(names)
    resources = _dict(proof_ir.get("resources"))
    plan = _dict(resources.get("pr_path_plan"))
    out: list[dict[str, Any]] = []
    for edge in _list(plan.get("edges")):
        if not isinstance(edge, dict):
            continue
        lemma = str(edge.get("lemma") or "")
        if lemma in wanted or _unqualified(lemma) in wanted:
            out.append(edge)
    return out


def _binding_for_name(resources: dict[str, Any], name: str) -> dict[str, Any]:
    bindings = _dict(resources.get("instantiation_bindings"))
    return _item_for_name(bindings, name)


def _binding_repair(resources: dict[str, Any], name: str) -> str:
    binding = _binding_for_name(resources, name)
    templates = [
        _dict(item) for item in _list(binding.get("instantiated_templates"))
        if isinstance(item, dict)
    ]
    for template in templates:
        tactic = str(template.get("tactic") or "")
        if tactic:
            return f"Probe the bound form: `{tactic}`."
    name_resolution = _dict(resources.get("name_resolution"))
    item = _item_for_name(name_resolution, name)
    action = str(item.get("signature_lookup_action") or "")
    if action:
        return f"Run `{action}` and then use the emitted typed template."
    return "Inspect the exact lemma signature before retrying the tactic."


def _binding_slot_repair(resources: dict[str, Any], name: str) -> str:
    binding = _binding_for_name(resources, name)
    missing = _missing_binding_slots(binding)
    ambiguous = _ambiguous_binding_slots(binding)
    pieces: list[str] = []
    if missing:
        pieces.append(
            "unresolved "
            + ", ".join(_slot_role(slot) for slot in missing[:4])
        )
    if ambiguous:
        pieces.append(
            "ambiguous "
            + ", ".join(_slot_role(slot) for slot in ambiguous[:4])
        )
    if not pieces:
        return ""
    return "Resolve typed argument slots by role: " + "; ".join(pieces) + "."


def _missing_binding_slots(binding: dict[str, Any]) -> list[dict[str, Any]]:
    out: list[dict[str, Any]] = []
    for slot in _list(binding.get("slots")):
        if not isinstance(slot, dict):
            continue
        if not _list(slot.get("candidates")):
            out.append(slot)
    return out


def _ambiguous_binding_slots(binding: dict[str, Any]) -> list[dict[str, Any]]:
    out: list[dict[str, Any]] = []
    for slot in _list(binding.get("slots")):
        if not isinstance(slot, dict):
            continue
        candidates = [
            _dict(item) for item in _list(slot.get("candidates"))
            if isinstance(item, dict)
        ]
        high_or_medium = [
            item for item in candidates
            if str(item.get("confidence") or "") in {"high", "medium"}
        ]
        if len(high_or_medium) > 1:
            out.append(slot)
    return out


def _slot_role(slot: dict[str, Any]) -> str:
    meta = _dict(slot.get("slot"))
    kind = str(meta.get("kind") or "argument")
    name = str(meta.get("name") or "")
    if kind == "module_arg":
        role = "module arg"
    elif kind == "memory_arg":
        role = "memory arg"
    elif kind == "value_arg":
        role = "value arg"
    elif kind == "type_arg":
        role = "type arg"
    else:
        role = kind.replace("_", " ") or "argument"
    return f"{role} `{name}`" if name else role


def _names_in_tactic(tactic: str) -> list[str]:
    names = []
    for name in re.findall(r"[A-Za-z_][A-Za-z0-9_'.]*(?:\.[A-Za-z_][A-Za-z0-9_'.]*)*", tactic):
        clean = name.strip(".")
        if clean in {
            "call",
            "ecall",
            "inline",
            "true",
            "false",
            "fun",
            "_",
        }:
            continue
        names.append(clean)
        unqualified = _unqualified(clean)
        if unqualified and unqualified != clean:
            names.append(unqualified)
    return _dedupe(names)


def _smt_argument_names(tactic: str) -> list[str]:
    match = re.search(r"\bsmt\s*\((?P<body>[^)]*)\)", tactic)
    if not match:
        return []
    body = match.group("body")
    names = [
        item for item in re.findall(r"[A-Za-z_][A-Za-z0-9_'.]*", body)
        if item not in {"true", "false", "by"}
    ]
    out: list[str] = []
    for name in names:
        out.append(name)
        unqualified = _unqualified(name)
        if unqualified and unqualified != name:
            out.append(unqualified)
    return _dedupe(out)


