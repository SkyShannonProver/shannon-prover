def _item_for_name(container: dict[str, Any], name: str) -> dict[str, Any]:
    if not name:
        return {}
    target = _unqualified(name)
    for item in _list(container.get("items")):
        if not isinstance(item, dict):
            continue
        item_name = str(item.get("name") or "")
        if item_name == name or _unqualified(item_name) == target:
            return item
    return {}


def _instantiation_repair(
    proof_ir: dict[str, Any],
    handle: dict[str, Any],
) -> str:
    binding = _binding_for_handle(proof_ir, handle)
    call_elaboration = _dict(binding.get("call_elaboration"))
    if call_elaboration.get("available"):
        direct = str(call_elaboration.get("direct_ecall_template") or "").strip()
        fallback = str(call_elaboration.get("tactic_template") or "").strip()
        if fallback and direct:
            return (
                f"Use the typed value-argument plumbing: `{fallback}`. "
                f"The direct `ecall` form `{direct}` is syntax-sensitive when "
                "arguments are program expressions, so treat it as a backup, "
                "not the first repair."
            )
        if fallback:
            return f"Probe the typed call elaboration: `{fallback}`."
    templates = [
        str(_dict(item).get("tactic") or item)
        for item in _list(binding.get("instantiated_templates"))
        if str(_dict(item).get("tactic") or item)
    ]
    if templates:
        return f"Probe the bound form: `{templates[0]}`."
    resolution = _resolution_for_handle(proof_ir, handle)
    action = str(resolution.get("signature_lookup_action") or "")
    if action:
        return f"Run `{action}` to inspect the exact argument slots."
    return "Inspect the exact lemma signature before trying another call form."


