"""Compiler-style diagnostics over ProofIR failures.

This module translates a rejected EasyCrypt tactic into ProofIR/ProgramIR
language.  It is deliberately not a trace-specific detector: it only reads the
typed resources that ProofIR already computed, such as live call handles,
frontier status, destructive moves, and name-resolution state.
"""
from __future__ import annotations

import re
from typing import Any

from core.easycrypt.analysis.ec_utils import (
    as_dict as _dict,
    as_list as _list,
    dedupe_present_strings as _dedupe,
    split_flat_conjuncts as _split_flat_conjuncts,
    unqualified as _unqualified,
)


def proof_ir_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    latest_tactic: str = "",
    latest_error: str = "",
) -> list[dict[str, Any]]:
    """Return Rust-style diagnostics for a rejected tactic."""
    tactic = latest_tactic.strip()
    error = latest_error.strip()
    if not tactic or not error:
        return []

    head = _tactic_head(tactic)
    if head in {"call", "ecall"}:
        return _call_failure_diagnostics(proof_ir, tactic=tactic, error=error)
    if head == "proc":
        return _proc_failure_diagnostics(proof_ir, tactic=tactic, error=error)
    if head == "seq":
        return _seq_failure_diagnostics(proof_ir, tactic=tactic, error=error)
    if head == "inline" and re.search(r"\binline\s+\*", tactic):
        return _inline_all_failure_diagnostics(
            proof_ir,
            tactic=tactic,
            error=error,
        )
    if head in {"rewrite", "apply", "byequiv", "have"}:
        return _lemma_failure_diagnostics(
            proof_ir,
            tactic=tactic,
            error=error,
            tactic_head=head,
        )
    if head == "smt":
        return _smt_failure_diagnostics(proof_ir, tactic=tactic, error=error)
    if head == "sim":
        return _sim_failure_diagnostics(proof_ir, tactic=tactic, error=error)
    if head == "while":
        return _while_failure_diagnostics(proof_ir, tactic=tactic, error=error)
    return []


def format_failure_diagnostics(diagnostics: list[dict[str, Any]]) -> str:
    """Format structured diagnostics for the text-oriented ``-diagnose`` tool."""
    if not diagnostics:
        return ""
    blocks: list[str] = []
    for diag in diagnostics:
        lines = ["=== ProofIR Diagnostic ==="]
        title = str(diag.get("title") or "")
        if title:
            lines.append(title)
        message = str(diag.get("message") or "")
        if message:
            lines.append(message)
        reason = str(diag.get("reason") or "")
        if reason and reason not in message:
            lines.append(f"Reason: {reason}")
        cause = str(diag.get("cause") or "")
        if cause:
            lines.append(f"Cause:  {cause}")
        repairs = [
            str(item) for item in _list(diag.get("repairs"))
            if str(item)
        ]
        if repairs:
            lines.append("Repair:")
            for idx, repair in enumerate(repairs, start=1):
                lines.append(f"  {idx}. {repair}")
        evidence = _dict(diag.get("evidence"))
        evidence_lines = _evidence_lines(evidence)
        if evidence_lines:
            lines.append("Evidence:")
            lines.extend(f"  - {line}" for line in evidence_lines)
        blocks.append("\n".join(lines))
    return "\n\n".join(blocks) + "\n"


def _call_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
) -> list[dict[str, Any]]:
    if _is_invariant_call(tactic):
        invariant_diags = _call_invariant_failure_diagnostics(
            proof_ir,
            tactic=tactic,
            error=error,
        )
        if invariant_diags:
            return invariant_diags

    handles = _callable_handles(proof_ir)
    matched = _matching_call_handles(handles, tactic)
    liveness = _dict(proof_ir.get("liveness"))
    destructive_moves = _list(proof_ir.get("destructive_moves"))
    live_sites = int(liveness.get("live_call_site_count") or 0)

    if matched:
        handle = matched[0]
        if str(handle.get("handle_role") or "") == "oracle_obligation_handle":
            return [_diag(
                code="proof_ir.failure.call_is_oracle_obligation",
                title="Call lemma belongs to an adversary oracle obligation",
                tactic=tactic,
                error=error,
                reason=(
                    "the lemma applies to an oracle subgoal generated by the "
                    "abstract adversary call, not to the current frontier call"
                ),
                cause=(
                    "The current pRHL frontier is the adversary call. EasyCrypt "
                    "will expose obligations for the adversary's oracle methods "
                    "only after an adversary invariant call."
                ),
                repairs=[
                    "Use the adversary invariant skeleton from ProofContextView, e.g. `call (_: <adversary invariant>).`.",
                    f"After that call generates oracle obligations, use `{tactic.strip()}` in the matching oracle subgoal.",
                    "Do not inline the adversary/oracle boundary merely to reach this handle.",
                ],
                evidence={
                    "lemma": str(handle.get("lemma") or ""),
                    "frontier_status": str(handle.get("frontier_status") or ""),
                    "handle_role": str(handle.get("handle_role") or ""),
                    "oracle_obligation_sources": _list(
                        handle.get("oracle_obligation_sources")
                    ),
                    "source_call_site_ids": _list(
                        handle.get("oracle_obligation_source_call_site_ids")
                    ),
                    "program_frontier": _program_frontier(proof_ir),
                },
            )]
        if handle.get("requires_cut_to_frontier"):
            return [_diag(
                code="proof_ir.failure.call_not_frontier",
                title="Call lemma is live before its frontier is exposed",
                tactic=tactic,
                error=error,
                reason=(
                    "the matching procedure call still exists; EasyCrypt's "
                    "current last-call frontier is a later statement"
                ),
                cause=(
                    "`call <lemma>` consumes the current frontier call. It "
                    "cannot jump over later statements merely because the "
                    "procedure call is live somewhere in the body."
                ),
                repairs=[
                    _repair_hint(handle) or (
                        "Use `wp`, `seq`, or a targeted transform to expose "
                        "the matching call as the last statement."
                    ),
                    "Re-run `-agent-view` after exposing the frontier, then probe the named call lemma.",
                    "Avoid `inline *` while this handle remains live.",
                ],
                evidence={
                    "lemma": str(handle.get("lemma") or ""),
                    "frontier_status": str(handle.get("frontier_status") or ""),
                    "live_call_site_ids": _list(handle.get("live_call_site_ids")),
                    "frontier_call_site_ids": _list(
                        handle.get("frontier_call_site_ids")
                    ),
                    "matched_call_sites": _list(handle.get("matched_call_sites")),
                    "program_diff_steps": _list(handle.get("program_diff_steps")),
                    "program_frontier": _program_frontier(proof_ir),
                },
            )]
        if _call_needs_instantiation(proof_ir, handle):
            return [_diag(
                code="proof_ir.failure.call_needs_instantiation",
                title="Call lemma needs typed instantiation",
                tactic=tactic,
                error=error,
                reason=(
                    "ProofIR knows the lemma signature has explicit module, "
                    "type, value, or memory slots"
                ),
                cause=(
                    "EasyCrypt could not infer all proof-term parameters from "
                    "the bare `call <lemma>` form."
                ),
                repairs=[
                    _instantiation_repair(proof_ir, handle),
                    _binding_slot_repair(
                        _dict(proof_ir.get("resources")),
                        str(handle.get("lemma") or ""),
                    ),
                    "Use the instantiated template only after `-try` accepts it.",
                    "If no binding is available, run the emitted `-sig` lookup before guessing arguments.",
                ],
                evidence={
                    "lemma": str(handle.get("lemma") or ""),
                    "name_resolution": _resolution_for_handle(proof_ir, handle),
                    "instantiation_binding": _binding_for_handle(proof_ir, handle),
                    "lemma_precondition_coverage": _dict(
                        _dict(_binding_for_handle(proof_ir, handle))
                        .get("call_elaboration")
                    ).get("lemma_precondition_coverage"),
                },
            )]
        return []

    if _inline_all_taken(destructive_moves) and live_sites == 0:
        return [_diag(
            code="proof_ir.failure.call_site_erased_by_inline_all",
            title="Call-site handle was erased before the call",
            tactic=tactic,
            error=error,
            reason="ProofIR sees no live call sites after a prior `inline *`",
            cause=(
                "`inline *` lowers procedure calls into bodies and erases "
                "the call-site handles that named equiv lemmas apply to."
            ),
            repairs=[
                "Undo to before `inline *` if the intended proof needs a named call lemma.",
                "At call-site layer, first try a frontier-ready `call <lemma>` action.",
                "If a wrapper must be opened, use targeted inline of that wrapper instead of global inline.",
            ],
            evidence={
                "destructive_moves": [
                    move for move in destructive_moves
                    if _dict(move).get("kind") == "inline_all"
                ],
                "live_call_site_count": live_sites,
            },
        )]

    if handles:
        return [_diag(
            code="proof_ir.failure.call_handle_mismatch",
            title="Call tactic does not match a live lemma handle",
            tactic=tactic,
            error=error,
            reason=(
                "ProofIR sees live call handles, but none match the lemma "
                "name used by this tactic"
            ),
            cause=(
                "The tactic may be using the wrong lemma name, a clone-renamed "
                "name, or a lemma for a different procedure call."
            ),
            repairs=[
                "Use the callable lemma names in ProofContextView before guessing.",
                "Run `-where <name>` or `-sig <name>` for the intended lemma.",
                "If the right call is live but not at frontier, expose it with `wp` or `seq` first.",
            ],
            evidence={
                "tactic_names": _names_in_tactic(tactic),
                "live_lemmas": [
                    str(handle.get("lemma") or "")
                    for handle in handles[:6]
                ],
            },
        )]
    return []


def _inline_all_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
) -> list[dict[str, Any]]:
    liveness = _dict(proof_ir.get("liveness"))
    live_lemmas = int(liveness.get("live_callable_lemma_count") or 0)
    if not live_lemmas:
        return []
    handles = _callable_handles(proof_ir)
    return [_diag(
        code="proof_ir.failure.inline_all_kills_live_handles",
        title="Global inline conflicts with live call handles",
        tactic=tactic,
        error=error,
        reason=(
            "`inline *` would erase call-site handles that ProofIR still "
            "considers live"
        ),
        cause=(
            "This is a phase-ordering problem: global lowering ran before "
            "the call-site abstraction had been used."
        ),
        repairs=[
            "Try the frontier-ready named call lemma before global inline.",
            "If the needed call is under a wrapper, use targeted inline of the wrapper only.",
            "Use `wp` or `seq` to expose a non-frontier call before calling its lemma.",
        ],
        evidence={
            "live_lemmas": [
                str(handle.get("lemma") or "")
                for handle in handles[:6]
            ],
            "live_callable_lemma_count": live_lemmas,
        },
    )]


def _smt_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
) -> list[dict[str, Any]]:
    if not re.search(r"cannot find lemma|unknown lemma", error, re.IGNORECASE):
        return []
    tactic_names = _smt_argument_names(tactic)
    if not tactic_names:
        return []
    unfoldables = _unfoldable_goal_heads(proof_ir)
    if not unfoldables:
        return []
    by_name = {
        _unqualified(str(item.get("name") or "")): item
        for item in unfoldables
        if item.get("name")
    }
    for name in tactic_names:
        clean = _unqualified(name)
        resolved = by_name.get(clean)
        if not resolved:
            continue
        return [_diag(
            code="proof_ir.failure.smt_argument_is_unfoldable_definition",
            title="SMT argument resolves as a definition, not a lemma",
            tactic=tactic,
            error=error,
            reason=(
                f"`{clean}` occurs in the current goal as an unfoldable "
                "op/pred/abbrev, but EasyCrypt resolves `smt(...)` arguments "
                "as lemmas or axioms."
            ),
            cause=(
                "Passing a definition name to `smt(...)` does not unfold it; "
                "EasyCrypt reports that no lemma with that name is available."
            ),
            repairs=[
                f"Use `rewrite /{clean}.` or `rewrite /{clean}; auto => />.` before `smt()`.",
                "Reserve `smt(name)` arguments for lemmas/axioms, not op/pred/abbrev definitions.",
                "If the name was intended as a lemma, run `-where NAME` to inspect its declaration kind.",
            ],
            evidence={
                "name": clean,
                "declaration_kind": str(resolved.get("declaration_kind") or ""),
                "unfold_tactic": str(
                    resolved.get("unfold_tactic") or f"rewrite /{clean}."
                ),
            },
        )]
    return []


def _proc_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
) -> list[dict[str, Any]]:
    if not re.search(r"expecting a goal of the form", error, re.I):
        return []
    current_layer = str(proof_ir.get("current_layer") or "")
    goal_type = str(proof_ir.get("goal_type") or proof_ir.get("goal_kind") or "")
    resources = _dict(proof_ir.get("resources"))
    program_ir = _dict(resources.get("program_ir"))
    handles = _dict(resources.get("handles"))
    frontend = _dict(handles.get("procedure_body_frontend"))
    obligation_stack = _dict(frontend.get("obligation_stack"))
    call_sites = _list(program_ir.get("call_sites"))
    if current_layer in {"procedure_body", "call_site"} and goal_type in {
        "pRHL",
        "equiv",
        "hoare",
        "phoare",
    }:
        return []
    return [_diag(
        code="proof_ir.failure.proc_needs_exposed_program_judgment",
        title="proc is blocked before a program judgment is exposed",
        tactic=tactic,
        error=error,
        reason=(
            "EasyCrypt rejected `proc` because the current goal is not a "
            "visible hoare/ehoare/phoare/equiv judgment"
        ),
        cause=(
            "The proof may be sitting on a wrapper obligation, side condition, "
            "or named predicate/invariant that contains the program judgment "
            "one layer underneath. `proc` cannot cross that wrapper by itself."
        ),
        repairs=[
            "Inspect the current goal shape before retrying `proc`; if the top-level goal is a named predicate/invariant, unfold that predicate first.",
            "Use `-goal-info` or the workspace current_goal text to identify whether the visible head is ambient logic, a predicate wrapper, or an actual program judgment.",
            "After the program judgment is exposed, retry `proc` or follow the displayed procedure/frontier map.",
        ],
        evidence={
            "current_layer": current_layer,
            "goal_type": goal_type,
            "program_call_site_count": len(call_sites),
            "obligation_stack_available": bool(obligation_stack.get("available")),
            "program_frontier": _program_frontier(proof_ir),
        },
    )]


def _seq_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
) -> list[dict[str, Any]]:
    attempted = _seq_counts(tactic)
    planned = _planned_seq_actions(proof_ir)
    if not attempted or not planned:
        return []
    exact = [
        action for action in planned
        if _seq_counts(str(action.get("tactic_hint") or "")) == attempted
    ]
    if exact:
        return []
    best = planned[0]
    planned_counts = _seq_counts(str(best.get("tactic_hint") or ""))
    if not planned_counts:
        return []
    return [_diag(
        code="proof_ir.failure.seq_misaligned_with_program_ir",
        title="seq split does not match the ProgramIR slice",
        tactic=tactic,
        error=error,
        reason=(
            "the attempted `seq` counts do not line up with the call-site "
            "slice that ProgramIR currently sees"
        ),
        cause=(
            "EasyCrypt `seq L R` cuts a concrete prefix on each side. If L/R "
            "do not expose the matched call frontier or preserve the live "
            "postcondition, the proof spawns the wrong obligations or fails."
        ),
        repairs=[
            f"Use the planned split: `{best.get('tactic_hint')}`.",
            "Fill the invariant with the `invariant_skeleton` conjuncts from the ProgramIR slice.",
            "Re-run `-agent-view` after the split, then apply the mapped call lemma.",
        ],
        evidence={
            "attempted_seq": {"left": attempted[0], "right": attempted[1]},
            "planned_seq": {"left": planned_counts[0], "right": planned_counts[1]},
            "planned_action": best,
            "program_slices": _program_slices(proof_ir)[:3],
        },
    )]


def _call_invariant_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
) -> list[dict[str, Any]]:
    named_frontier = [
        handle for handle in _callable_handles(proof_ir)
        if handle.get("callable_now") and str(handle.get("handle_role") or "") != "oracle_obligation_handle"
    ]
    if named_frontier:
        return [_diag(
            code="proof_ir.failure.call_invariant_when_named_handle_available",
            title="Invariant call is re-proving a live named equivalence",
            tactic=tactic,
            error=error,
            reason=(
                "ProofIR sees a named equiv lemma at the current call frontier"
            ),
            cause=(
                "`call (_: Inv)` asks EasyCrypt to prove the procedure "
                "correspondence from an invariant. A named `call <lemma>` "
                "usually preserves abstraction and avoids manual invariant gaps."
            ),
            repairs=[
                f"Probe `{_named_call_tactic(named_frontier[0])}` first.",
                "Use `call (_: Inv)` only if no named call lemma matches the frontier.",
                "If the named call fails, inspect its signature before falling back to a handcrafted invariant.",
            ],
            evidence={
                "lemma": str(named_frontier[0].get("lemma") or ""),
                "frontier_status": str(named_frontier[0].get("frontier_status") or ""),
                "matched_call_sites": _list(named_frontier[0].get("matched_call_sites")),
            },
        )]

    missing = _missing_invariant_conjuncts(proof_ir, tactic)
    if missing:
        return [_diag(
            code="proof_ir.failure.call_invariant_missing_conjuncts",
            title="Call invariant is missing live ProgramIR conjuncts",
            tactic=tactic,
            error=error,
            reason=(
                "the attempted invariant omits conjuncts that ProgramIR keeps "
                "live for the current call-site slice"
            ),
            cause=(
                "Call invariants must preserve the variables and relations "
                "needed by the call lemma postcondition and the remaining suffix."
            ),
            repairs=[
                "Extend the invariant with: " + " /\\ ".join(missing[:6]) + ".",
                "Prefer the generated `call_invariant_skeleton` or `program_edit_script_v2` slice invariant.",
                "Probe the completed invariant with `-try` before committing it.",
            ],
            evidence={
                "missing_conjuncts": missing,
                "program_slices": _program_slices(proof_ir)[:3],
            },
        )]
    return []


def _sim_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
) -> list[dict[str, Any]]:
    frontend = _procedure_frontend(proof_ir)
    one_sided = _dict(frontend.get("one_sided_call_site_summary"))
    if one_sided.get("available"):
        return [_diag(
            code="proof_ir.failure.sim_blocked_by_one_sided_call_site",
            title="sim is blocked by a one-sided call frontier",
            tactic=tactic,
            error=error,
            reason=(
                "ProcedureIR sees a call site on only one side of the current "
                "program slice"
            ),
            cause=(
                "`sim` synchronizes aligned residual programs. A one-sided "
                "call usually needs side-specific call/ecall, wp, or conseq "
                "before the residual result relation can be closed."
            ),
            repairs=[
                "Inspect the `procedure_one_sided_call_site_map` item in agent-view.",
                "If using side-specific call/ecall, keep the returned expression bridge explicit.",
                "If the other side is already pure, use wp/conseq to expose the result relation before auto/smt.",
            ],
            evidence={
                "sites": _list(one_sided.get("sites"))[:6],
                "post_mentions_result": bool(one_sided.get("post_mentions_result")),
                "result_expression_map": _dict(
                    one_sided.get("result_expression_map")
                ),
            },
        )]
    region = _dict(frontend.get("asymmetric_instrumentation_region"))
    if region.get("available"):
        return [_diag(
            code="proof_ir.failure.sim_blocked_by_asymmetric_instrumentation",
            title="sim is blocked by one-sided instrumentation state",
            tactic=tactic,
            error=error,
            reason=(
                "ProcedureIR sees a shared program core plus extra visible state "
                "updates on one side"
            ),
            cause=(
                "`sim` requires synchronized residual structure. Extra monitor/log/"
                "counter updates can be semantically harmless but still require a "
                "cut, branch split, swap, or consequence step that carries the live "
                "postcondition facts."
            ),
            repairs=[
                "Inspect the `procedure_asymmetric_instrumentation_map` item in agent-view.",
                "Check whether the current seq/call invariant preserves the displayed one-sided extra state if the post mentions it.",
                "After the relevant guard/alignment obligation is visible, use rcond/case/swap/conseq as a local transform and re-run agent-view.",
            ],
            evidence={
                "instrumented_side": str(region.get("instrumented_side") or ""),
                "shared_written_vars": _list(region.get("shared_written_vars"))[:8],
                "left_extra_written_vars": _list(region.get("left_extra_written_vars"))[:8],
                "right_extra_written_vars": _list(region.get("right_extra_written_vars"))[:8],
                "proof_relevant_extra_vars": _list(region.get("proof_relevant_extra_vars"))[:8],
            },
        )]
    result_map = _dict(frontend.get("result_expression_map"))
    if result_map.get("direct_res_equality_risky"):
        return [_diag(
            code="proof_ir.failure.sim_blocked_by_result_expression_bridge",
            title="sim needs an explicit result-expression bridge",
            tactic=tactic,
            error=error,
            reason=(
                "ProcedureIR sees both sides defining res/result through "
                "different visible expressions"
            ),
            cause=(
                "The postcondition mentions res/result, but the current slice "
                "does not expose a literal same-expression result equality."
            ),
            repairs=[
                "Inspect the `procedure_result_expression_map` item in agent-view.",
                "Preserve or prove the derived relation between the displayed expressions before closing with sim/skip/auto.",
                "Re-run agent-view after wp/call/seq because the result-producing statement may move.",
            ],
            evidence={
                "left": _dict(result_map.get("left")),
                "right": _dict(result_map.get("right")),
                "relation_shape": str(result_map.get("relation_shape") or ""),
            },
        )]
    return []


def _while_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
) -> list[dict[str, Any]]:
    low = str(error or "").lower()
    if "last instruction" not in low and "last statement" not in low:
        return []
    frontend = _procedure_frontend(proof_ir)
    plan = _dict(frontend.get("frontier_plan"))
    loops = [
        _dict(item) for item in _list(frontend.get("loop_frontiers"))
        if isinstance(item, dict)
    ]
    return [_diag(
        code="proof_ir.failure.while_not_at_suffix_frontier",
        title="while is not at the current EasyCrypt suffix frontier",
        tactic=tactic,
        error=error,
        reason=(
            "EasyCrypt can apply `while` only when the loop is the active last "
            "instruction/suffix frontier for the chosen side"
        ),
        cause=(
            "There is still visible suffix code after the loop, or the loop is "
            "behind straight-line assignments. A loop invariant may be right, "
            "but the procedure slice must expose the loop frontier first."
        ),
        repairs=[
            "Use `wp` over trailing suffix statements or `sp` over leading assignments, then re-run agent-view.",
            "If the loop has phases, inspect the `splitwhile` candidate before committing a while invariant.",
            "Keep the invariant candidate, but wait until ProcedureIR reports the loop as the active frontier.",
        ],
        evidence={
            "frontier_kind": str(plan.get("frontier_kind") or ""),
            "loop_frontiers": loops[:3],
            "region_summary": _list(plan.get("region_summary"))[:6],
        },
    )]


def _lemma_failure_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
    tactic_head: str,
) -> list[dict[str, Any]]:
    names = _names_in_tactic(tactic)
    if tactic_head == "rewrite" and _looks_like_rewrite_match_error(error):
        direction = _rewrite_direction_diagnostics(
            proof_ir,
            tactic=tactic,
            error=error,
            tactic_names=names,
        )
        if direction:
            return direction
    resources = _dict(proof_ir.get("resources"))
    name_resolution = _dict(resources.get("name_resolution"))
    matches = _matching_resolution_items(name_resolution, names)
    if matches:
        item = matches[0]
        name = str(item.get("name") or "")
        if item.get("exact_signature_known") and item.get("requires_instantiation"):
            return [_diag(
                code=f"proof_ir.failure.{tactic_head}_needs_instantiation",
                title=f"{tactic_head} lemma needs typed instantiation",
                tactic=tactic,
                error=error,
                reason=(
                    "ProofIR resolved the lemma signature and it has explicit "
                    "module, type, value, or memory parameters"
                ),
                cause=(
                    "EasyCrypt could not infer all proof-term parameters from "
                    f"the bare `{tactic_head} <lemma>` form."
                ),
                repairs=[
                    _binding_repair(resources, name),
                    _binding_slot_repair(resources, name),
                    "Probe the instantiated form with `-try` before committing it.",
                    "If any slot is still `_`, inspect the current goal and exact signature before guessing.",
                ],
                evidence={
                    "lemma": name,
                    "tactic_names": names,
                    "name_resolution": item,
                    "instantiation_binding": _binding_for_name(resources, name),
                    "missing_slots": [
                        _slot_role(slot)
                        for slot in _missing_binding_slots(
                            _binding_for_name(resources, name)
                        )
                    ],
                    "ambiguous_slots": [
                        _slot_role(slot)
                        for slot in _ambiguous_binding_slots(
                            _binding_for_name(resources, name)
                        )
                    ],
                },
            )]
        if not item.get("exact_signature_known"):
            action = str(item.get("signature_lookup_action") or "")
            return [_diag(
                code=f"proof_ir.failure.{tactic_head}_needs_name_resolution",
                title=f"{tactic_head} lemma needs name resolution",
                tactic=tactic,
                error=error,
                reason=(
                    "ProofIR cannot verify this lemma's exact signature from "
                    "the current local context"
                ),
                cause=(
                    "The name may come from a clone/import boundary or an "
                    "abstract/local theory whose argument order is not visible."
                ),
                repairs=[
                    f"Run `{action}` before retrying this tactic." if action else
                    "Run `-sig <lemma>` before retrying this tactic.",
                    "Use the typed template emitted by ProofIR if signature lookup succeeds.",
                    "Prefer a resolved candidate from ProofContextView over trial-and-error arity guesses.",
                ],
                evidence={
                    "lemma": name,
                    "tactic_names": names,
                    "name_resolution": item,
                },
            )]

    if tactic_head == "rewrite":
        direction = _rewrite_direction_diagnostics(
            proof_ir,
            tactic=tactic,
            error=error,
            tactic_names=names,
        )
        if direction:
            return direction
        return _rewrite_phase_mismatch_diagnostics(
            proof_ir,
            tactic=tactic,
            error=error,
            tactic_names=names,
        )
    if tactic_head == "byequiv":
        return _byequiv_phase_mismatch_diagnostics(
            proof_ir,
            tactic=tactic,
            error=error,
            tactic_names=names,
        )
    return []


def _looks_like_rewrite_match_error(error: str) -> bool:
    low = str(error or "").lower()
    return any(
        token in low
        for token in (
            "left-hand side",
            "does not match",
            "no matching",
            "cannot rewrite",
            "not a rewrite",
        )
    )


def _rewrite_phase_mismatch_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
    tactic_names: list[str],
) -> list[dict[str, Any]]:
    layer = str(proof_ir.get("current_layer") or "")
    resources = _dict(proof_ir.get("resources"))
    handles = _dict(resources.get("handles"))
    pr_candidates = [
        str(item) for item in _list(handles.get("pr_rewrite_candidates"))
        if str(item)
    ]
    if layer != "pr" and pr_candidates:
        return [_diag(
            code="proof_ir.failure.rewrite_wrong_layer",
            title="Rewrite handle belongs to a different proof layer",
            tactic=tactic,
            error=error,
            reason=(
                "ProofIR sees Pr rewrite handles, but the active goal is no "
                "longer at the Pr layer"
            ),
            cause=(
                "A prior tactic likely lowered the goal before the Pr rewrite "
                "or have-chain handle was consumed."
            ),
            repairs=[
                "Undo to the Pr-layer goal if the rewrite is the intended bridge.",
                "Use the current layer's candidate menu if the lowering was intentional.",
                "Re-run `-agent-view` and follow the current `phase_prefer` list.",
            ],
            evidence={
                "current_layer": layer,
                "tactic_names": tactic_names,
                "pr_rewrite_candidates": pr_candidates[:6],
            },
        )]
    return []


def _rewrite_direction_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
    tactic_names: list[str],
) -> list[dict[str, Any]]:
    if str(proof_ir.get("current_layer") or "") != "pr":
        return []
    attempted_negative = bool(re.search(r"\brewrite\s+-", tactic))
    edges = _pr_path_edges_for_names(proof_ir, tactic_names)
    if not edges:
        return []
    wanted_direction = "backward" if not attempted_negative else "forward"
    alternative = next(
        (
            edge for edge in edges
            if str(edge.get("direction") or "") == wanted_direction
        ),
        {},
    )
    if not alternative:
        return []
    action = str(alternative.get("action_hint") or "")
    if not action:
        return []
    return [_diag(
        code="proof_ir.failure.rewrite_direction_mismatch",
        title="Rewrite direction does not match the Pr path edge",
        tactic=tactic,
        error=error,
        reason=(
            "the lemma is known as a Pr rewrite edge, but the attempted "
            "direction does not move the current endpoint toward the target"
        ),
        cause=(
            "Pr bridge lemmas are often usable in both directions. The source "
            "and target games in the current goal determine whether `rewrite` "
            "or `rewrite -` is the right edge."
        ),
        repairs=[
            f"Try the path edge direction: `{action}`.",
            "If the lemma has explicit arguments, run the ProofIR `-where`/typed template first and then flip the rewrite direction.",
            "Stay at the Pr layer until the bridge/path action is consumed.",
        ],
        evidence={
            "tactic_names": tactic_names,
            "attempted_negative": attempted_negative,
            "suggested_edge": alternative,
        },
    )]


def _byequiv_phase_mismatch_diagnostics(
    proof_ir: dict[str, Any],
    *,
    tactic: str,
    error: str,
    tactic_names: list[str],
) -> list[dict[str, Any]]:
    layer = str(proof_ir.get("current_layer") or "")
    resources = _dict(proof_ir.get("resources"))
    handles = _dict(resources.get("handles"))
    pr_normal = _dict(handles.get("pr_normal_form"))
    pr_candidates = [
        str(item) for item in _list(handles.get("pr_rewrite_candidates"))
        if str(item)
    ]
    pr_path = _dict(_dict(handles.get("pr_path_plan")).get("recommended_path"))
    if layer == "pr" and (pr_normal.get("available") or pr_candidates or pr_path):
        repairs = []
        if pr_normal.get("available"):
            repairs.append(
                f"If the outer Pr shell is the blocker, probe normalization `{pr_normal.get('recommended_tactic') or 'congr.'}`."
            )
        if pr_candidates:
            repairs.append(
                "If pursuing a Pr rewrite/path route, relevant handle(s) include: "
                + ", ".join(pr_candidates[:4])
                + "."
            )
        if pr_path:
            repairs.append("A planned Pr have/rewrite path is available; test its next hop only if it matches the current endpoint.")
        repairs.append("If direct program equivalence remains cleaner, refine the byequiv pre/post or inspect the pRHL goal instead of exhausting every Pr handle.")
        return [_diag(
            code="proof_ir.failure.byequiv_before_pr_frontend",
            title="byequiv failed while Pr-route context is still visible",
            tactic=tactic,
            error=error,
            reason=(
                "ProofIR sees Pr normalization, rewrite, or path options as "
                "route context, not mandatory prerequisites"
            ),
            cause=(
                "`byequiv` opens pRHL. On some goals this is intended; on others "
                "a matching Pr handle gives a smaller proof."
            ),
            repairs=repairs,
            evidence={
                "current_layer": layer,
                "tactic_names": tactic_names,
                "pr_normal_form": pr_normal,
                "pr_rewrite_candidates": pr_candidates[:6],
                "pr_path_recommended": pr_path,
            },
        )]
    if layer in {"procedure_body", "ambient_logic"}:
        return [_diag(
            code="proof_ir.failure.byequiv_after_lowering",
            title="byequiv is being tried after the proof was lowered",
            tactic=tactic,
            error=error,
            reason=(
                "ProofIR classifies the current state below the Pr/pRHL module "
                "layer where `byequiv` usually opens or bridges games"
            ),
            cause=(
                "The proof may have already committed to procedure-body or "
                "residual-logic obligations."
            ),
            repairs=[
                "Undo to the Pr or pRHL module layer if `byequiv` is the intended opener.",
                "Otherwise continue with procedure-body tactics such as wp/sp/seq/rnd.",
                "Read ProofContextView's current layer and candidate menu before retrying.",
            ],
            evidence={
                "current_layer": layer,
                "tactic_names": tactic_names,
            },
        )]
    return []


def _diag(
    *,
    code: str,
    title: str,
    tactic: str,
    error: str,
    reason: str,
    cause: str,
    repairs: list[str],
    evidence: dict[str, Any],
) -> dict[str, Any]:
    return {
        "code": code,
        "severity": "warning",
        "title": title,
        "message": f"TACTIC REJECTED: `{tactic}`. Reason: {reason}.",
        "tactic": tactic,
        "latest_error": error,
        "reason": reason,
        "cause": cause,
        "repairs": repairs,
        "evidence": evidence,
    }


