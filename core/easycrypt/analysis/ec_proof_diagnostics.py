    if frontier:
        lines.append(f"frontier_status: {frontier}")
    role = str(evidence.get("handle_role") or "")
    if role:
        lines.append(f"handle_role: {role}")
    missing = [
        str(item) for item in _list(evidence.get("missing_slots"))
        if str(item)
    ]
    if missing:
        lines.append("missing slots: " + ", ".join(missing[:6]))
    ambiguous = [
        str(item) for item in _list(evidence.get("ambiguous_slots"))
        if str(item)
    ]
    if ambiguous:
        lines.append("ambiguous slots: " + ", ".join(ambiguous[:6]))
    coverage = _dict(evidence.get("lemma_precondition_coverage"))
    if coverage.get("available"):
        lines.append(
            "lemma precondition coverage: "
            f"{int(coverage.get('covered_by_exlim_count') or 0)}/"
            f"{int(coverage.get('pre_atom_count') or 0)} exlim-bound, "
            f"{int(coverage.get('expected_residual_count') or 0)} residual"
        )
    live = [
        str(item) for item in _list(evidence.get("live_lemmas"))
        if str(item)
    ]
    if live:
        lines.append("live lemmas: " + ", ".join(live[:6]))
    live_ids = [
        str(item) for item in _list(evidence.get("live_call_site_ids"))
        if str(item)
    ]
    if live_ids:
        lines.append("live call sites: " + ", ".join(live_ids[:4]))
    frontier_ids = [
        str(item) for item in _list(evidence.get("frontier_call_site_ids"))
        if str(item)
    ]
    if frontier_ids:
        lines.append("frontier call sites: " + ", ".join(frontier_ids[:4]))
    live_count = evidence.get("live_call_site_count")
    if live_count is not None:
        lines.append(f"live_call_site_count: {live_count}")
    return lines


