def _seq_counts(text: str) -> tuple[int, int] | None:
    match = re.search(r"\bseq\s+(\d+)\s+(\d+)\b", str(text or ""))
    if not match:
        return None
    return (int(match.group(1)), int(match.group(2)))


def _planned_seq_actions(proof_ir: dict[str, Any]) -> list[dict[str, Any]]:
    actions: list[dict[str, Any]] = []
    for item in _program_slices(proof_ir):
        if not isinstance(item, dict):
            continue
        invariant = _dict(item.get("invariant_skeleton"))
        for action in _list(item.get("phase_order")):
            if not isinstance(action, dict):
                continue
            hint = str(action.get("tactic_hint") or "")
            if not _seq_counts(hint):
                continue
            copied = dict(action)
            copied["slice_id"] = str(item.get("id") or "")
            copied["invariant_skeleton"] = invariant
            actions.append(copied)
    return actions


